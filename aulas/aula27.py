"""
Fatiamento de Strings
012345678
Olá mundo
-987654321

Fatiamento [i:f:p] [::]
i de inicio, f de final, p de passo

a função len vai retornar a quantidade de caracteres da string

"""

variavel = 'Olá mundo'
print(variavel[0:6])

print(variavel[0:6:2])

print(len(variavel))

print(len(variavel[0:9:1]))